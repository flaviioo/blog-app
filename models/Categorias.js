import { Schema as _Schema, model } from 'mongoose'
const Schema = _Schema

const Categoria = new Schema({
    nome: {
        type: String,
        required: true
    },
    slug: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now()
    }
})

model('categorias', Categoria)